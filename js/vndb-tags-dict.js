    if (!Array.isArray(englishTagsArray)) {
        return englishTagsArray;
    }
    
    return englishTagsArray.map(tag => {
        if (tag && tag.name) {
            const translatedName = vndbTagDict[tag.name] || tag.name;
            return {
                ...tag,
                name: translatedName,
                originalName: tag.name
            };
        }
        return tag;
    });
